    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String filecontent=jTextArea1.getText();
        Connection connection = ModelData.getConnection();
      
        try {
            try (BufferedWriter writer = new BufferedWriter(new FileWriter("output.txt",true))) {
                
                try {
                  String sql = "select customer.customer_name, A.OrderID,order_items.ItemID,quantity,storeitems.name,bill,PaymentType\n"
                     +"from\n"+ 
                     "( select OrderID,CustID from orders where CustID ="+String.valueOf(ModelData.userID)+" ) as A\n"+
                     "INNER JOIN\n"+ 
                     "order_items on A.OrderID = order_items.OrderID\n"+
                     "INNER JOIN storeitems on storeitems.ItemID = order_items.ItemID\n"+
                     "INNER JOIN orders on orders.OrderID = A.OrderID\n"+
                     "INNER JOIN customer on customer.customerID = A.CustID\n";
        
        java.sql.Statement stmt= connection.createStatement();  
        ResultSet rs=stmt.executeQuery(sql);
         String details = "";
         
         String orderID = "";
        while(rs.next())  
        {
             if(!orderID.equals(rs.getString(1)))
             {
                 String customer_name=rs.getString(1);
                 orderID = rs.getString(2);
                 details+="customer name:"+customer_name+"\nOrder ID:"+orderID+"\nTotal Bill:"+String.format("%.2f",rs.getDouble(6))+"\nPayment Method:"+rs.getString(7)+"\n\nOrder Details:\n\n";
             }
             details+="Item ID:"+rs.getInt(3)+"\nName:"+rs.getString(5)+"\nQuantity:"+rs.getInt(4)+"\n\n";
        }
        writer.write(details);
        writer.write("\n");
                } catch (SQLException ex) {
                     ex.printStackTrace();
                }
                if(satified.isSelected()||verySatified.isSelected()||notSatified.isSelected()){
                writer.write("customer Satisfaction:  ");
                if(satified.isSelected()){ writer.write("Satified ");} 
                 else if(verySatified.isSelected()){writer.write("Very Satified ");}
                 else if(notSatified.isSelected()){writer.write("Not Satified ");}
                writer.write("\n");
                writer.write("customer Extra coments:  ");
                writer.write(filecontent);
                writer.write("\n");
                writer.write("--------------------");
                  writer.write("\n");
                writer.close();
              JOptionPane.showMessageDialog(null,"we recived your Review. thank you");}
                else
                {
                    JOptionPane.showMessageDialog(null,"please choose an option then try again");
                }
            }
        } 
        catch(SecurityException ex)
        {
            System.out.println("Write Permission Denied!");
        }
        catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

