    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if(jTextField1.getText().isBlank() || jTextField3.getText().isBlank() || String.valueOf(jPasswordField1.getPassword()).isBlank())
        {
            JOptionPane.showMessageDialog(null,"Either one or more fields is empty. Kindly, fill all the fields to proceed");
        }
        else if(!jTextField1.getText().matches(".*"+"@"+".*"+".com"))
        {
            JOptionPane.showMessageDialog(null,"The specified email format is incorrect. (Format: abc@xyz.com)");
        }
        
        else
        {
            Connection connection = ModelData.getConnection();
            if(connection != null)
            {
                try
                {
                    boolean found = false;
                    java.sql.Statement stmt= connection.createStatement();  
                    ResultSet rs=stmt.executeQuery("select Email from customer");
                    
                    while(rs.next() && !found)  
                    {
                        if(rs.getString(1).equals(jTextField1.getText()))
                        {
                            found = true;
                        }
                    }
                    if(found)
                    {
                        JOptionPane.showMessageDialog(null,"The email entered already exists in the database.");
                    }
                    else
                    {
                        stmt.executeUpdate("INSERT INTO customer VALUES (NULL,\""+jTextField3.getText()+"\",\""+jTextField1.getText()+"\",\""+String.valueOf(jPasswordField1.getPassword())+"\")");
                        CustomerLogin login = new CustomerLogin();
                        login.setVisible(true);
                        this.dispose();
                    }
                        }
                        catch(SQLException e)
                        {
                           System.out.println("Database Error!");
                        }
                        catch(Exception e)
                        {
                            e.printStackTrace();
                        }
                }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

