    public void update()throws SQLException {
        Connection connection = ModelData.getConnection();
        if (connection != null) {
            
                java.sql.Statement stmt = connection.createStatement();
                ResultSet rs = stmt.executeQuery("select * from storeitems");
                String details = "";
                while (rs.next()) {
                    details += "Item ID:" + rs.getInt(1) + "\nName:" + rs.getString(2) + "\nType:" + rs.getString(3) + "\nPrice:" + rs.getInt(4) + "\nQuantity:" + rs.getInt(5) + "\n\n";
                }

                jTextArea2.setText(details);

           
        } 
    }
