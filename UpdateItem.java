    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTextField6.getText().matches("[0-9]+")) 
        {
            if (jTextField4.getText().matches("[0-9]+")) {
                if (jTextField3.getText().matches("[0-9]+")) {
                    if (!jTextField1.getText().isBlank()) {
                        Connection connection = ModelData.getConnection();
                        if (connection != null) {
                            try {
                                java.sql.Statement stmt = connection.createStatement();

                                int updated = stmt.executeUpdate("update storeitems set name = \"" + jTextField1.getText() + "\",price =" + jTextField4.getText() + ",quantityAvailable = " + jTextField3.getText() + " where ItemID =" + jTextField6.getText());

                                if (updated == 0) {
                                    JOptionPane.showMessageDialog(null, "No item found with the provided ID!");
                                } else {
                                    update();
                                    JOptionPane.showMessageDialog(null, "The item has been successfully updated!");
                                }

                            } 
                            catch(SQLException e){
                            System.out.println("Database Error!");
                             }
                            catch (Exception e) {
                                e.printStackTrace();
                            }
                        } 
                    } else {
                        System.out.println("Item name cannot be left blank!");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Quantity available can only consist of numeric values!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Price can only consist of numeric values!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Item ID can only consist of numeric values!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

