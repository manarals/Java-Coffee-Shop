    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextField2.getText().matches("[0-9]+"))
        {
        Connection connection = ModelData.getConnection();
            if(connection != null)
            {
                try
                {
                    java.sql.Statement stmt= connection.createStatement();  
                    
                        int deleted = stmt.executeUpdate("delete from storeitems where ItemID ="+jTextField2.getText());
                   
                     if(deleted == 0)
                     {
                        JOptionPane.showMessageDialog(null, "No item found with the provided ID!");
                     }
                     else
                     {  
                        update();
                        JOptionPane.showMessageDialog(null, "The item has been successfully deleted!");
                     }
                    
                }
                catch(InputMismatchException e)
                {
                System.out.println("Invalid Entry!");
                }
                catch(SQLException e)
                {
                 System.out.println("Database Error!");
                }
                catch(Exception e)
                {
                  e.printStackTrace();
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"ID can only consist of numeric values!");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

