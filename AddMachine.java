    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!jTextField1.getText().isBlank() && !jTextField3.getText().isBlank() && !jTextField4.getText().isBlank() && !jTextField6.getText().isBlank() && !jTextField7.getText().isBlank())
        {
            if(jTextField6.getText().matches("[0-9]+"))
            {

                if(jTextField7.getText().matches("[0-9]+"))
                {
                    Connection connection = ModelData.getConnection();
                    if(connection != null)
                    {
                        try
                        {

                            java.sql.Statement stmt= connection.createStatement();

                            stmt.executeUpdate("INSERT INTO coffee_machines VALUES ("+String.valueOf(ID)+","+"\""+jTextField1.getText()+"\","+"\""+jTextField3.getText()+"\","+"\""+jTextField4.getText()+"\","+jTextField6.getText()+","+jTextField7.getText()+")");

                            JOptionPane.showMessageDialog(null, "Machine has been added successfully!");
                            AdminMenu adminMenu = new AdminMenu();
                            adminMenu.setVisible(true);
                            this.dispose();
                        }
                        catch(SQLException e)
                        {
                            System.out.println("Database Error!");
                        }
                        catch(Exception e)
                        {
                            e.printStackTrace();
                        }
                    }

                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Voltage can only contain numbers!");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Weight can only contain numbers!");
            }

        }
        else
        {
            JOptionPane.showMessageDialog(null, "Either one or more fields are empty!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

